// Wheel Velocity
void setWheelVelocity(float lnr_x, float ang_z, float r, double *left_vel, double *right_vel){

	// r is in m



	*left_vel = (lnr_x - (r*ang_z))*1.1542*sample_time/100;
	*right_vel = (lnr_x + (r*ang_z))*1.1542*sample_time/100;

}


